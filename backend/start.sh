#!/bin/bash

# -----------------------------------------------------------------
# Скрипт запуска backend FastAPI для платформы анализа тональности
# -----------------------------------------------------------------

# Прерывать выполнение при ошибке
set -e

# --------------------------------------
# 1. Активация виртуального окружения (для локального запуска)
# --------------------------------------
if [ -d "venv" ]; then
    echo "[INFO] Активируем виртуальное окружение..."
    source venv/bin/activate
fi

# --------------------------------------
# 2. Создание папки для загруженных файлов
# --------------------------------------
UPLOAD_DIR="./app/data/uploads"
if [ ! -d "$UPLOAD_DIR" ]; then
    echo "[INFO] Создаем директорию для загрузки файлов: $UPLOAD_DIR"
    mkdir -p "$UPLOAD_DIR"
fi

# --------------------------------------
# 3. Установка Python зависимостей
# --------------------------------------
if [ -f "requirements.txt" ]; then
    echo "[INFO] Устанавливаем Python зависимости..."
    pip install --upgrade pip
    pip install --no-cache-dir -r requirements.txt
fi

# --------------------------------------
# 4. Запуск FastAPI сервера через uvicorn
# --------------------------------------
echo "[INFO] Запуск FastAPI backend на 0.0.0.0:8000..."
uvicorn app.main:app --host 0.0.0.0 --port 8000 --reload
